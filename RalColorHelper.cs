        public static (RalColor Color, double Diff)? GetClosestRalColor(string hexTarget)
        {
            try
            {
                if (!hexTarget.StartsWith("#")) hexTarget = "#" + hexTarget;
                var targetUnicolour = new Wacton.Unicolour.Unicolour(hexTarget);
                RalColor closest = null;
                double minDiff = double.MaxValue;

                foreach (var ral in Colors)
                {
                    var ralUnicolour = new Wacton.Unicolour.Unicolour(ral.Hex);
                    var diff = System.Math.Sqrt(
                        System.Math.Pow(targetUnicolour.Lab.L - ralUnicolour.Lab.L, 2) + 
                        System.Math.Pow(targetUnicolour.Lab.A - ralUnicolour.Lab.A, 2) + 
                        System.Math.Pow(targetUnicolour.Lab.B - ralUnicolour.Lab.B, 2));
                    
                    if (diff < minDiff)
                    {
                        minDiff = diff;
                        closest = ral;
                    }
                }
                return (closest, minDiff);
            }
            catch
            {
                return null;
            }
        }
